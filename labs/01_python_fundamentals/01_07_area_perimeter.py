'''

Write the necessary code to display the area and perimeter of a rectangle that has a width of 2.4 and a height of 6.4.

'''
width = 2.4
height = 6.4
area_of_rectangle = width * height
print("Area of rectangle is: ", area_of_rectangle)
perimeter_of_rectangle = 2 * (width + height)
print("Perimeter of rectangle is:", perimeter_of_rectangle)
